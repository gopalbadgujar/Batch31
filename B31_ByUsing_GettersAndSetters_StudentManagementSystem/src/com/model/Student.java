package com.model;

public class Student {

	private int id;
	private String name;
	private String address;
	
	public void setId (int sid) {
		id = sid;
	}
	public int getId() {
		return id;
	}
	
	public void setName(String sname) {
		name = sname;
	}
	public String getName() {
		return name;
	}
	
	public void setAddress(String saddress) {
		address = saddress;
	}
	public String getAddress() {
		return address;
	}
	
	
}
